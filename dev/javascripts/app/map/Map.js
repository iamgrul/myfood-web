class Map{
    
    constructor(setting){
        let $map = $(setting.id),
            lat = $map.data('lat'),
            lng = $map.data('lng')

        let mapOptions = {
            zoom: 16,
            center: new google.maps.LatLng(lat,lng),
            styles: map_style,
            scrollwheel:true,
            mapTypeControl:false,
            streetViewControl:false,
            zoomControl: false,
            panControl:true,
            disableDefaultUI: true
        }


        let map = new google.maps.Map($map[0], mapOptions)

        let pinIcon = new google.maps.MarkerImage(
            myfood.img+"/misc/pin.png",
            null,
            null,
            null,
            new google.maps.Size(31, 41)
        )

        let marker = new google.maps.Marker({
            position: new google.maps.LatLng(lat, lng),
            map: map,
            icon: pinIcon
        })

    }
}
